    // [START refresh_token]
    func tokenRefreshNotification(_ notification: Notification) {
        if let refreshedToken = FIRInstanceID.instanceID().token() {
            print("InstanceID token: \(refreshedToken)")
            // Send Token to the Cloud backend for update
            sendRegistrationToServer(token: refreshedToken)
        }
        
        // Connect to FCM since connection may have failed when attempted before having a token.
        connectToFcm()
    }
    // [END refresh_token]
    // [START connect_to_fcm]
    func connectToFcm() {
        // Won't connect since there is no token
        guard FIRInstanceID.instanceID().token() != nil else {
            return
        }
        
        // Disconnect previous FCM connection if it exists.
        FIRMessaging.messaging().disconnect()
        
        FIRMessaging.messaging().connect { (error) in
            if error != nil {
                print("Unable to connect with FCM. \(error)")
            } else {
                print("Connected to FCM.")
            }
        }
    }
    // [END connect_to_fcm]
    
    func sendRegistrationToServer(token: String) {
        let userType = Globals.globObj.getUserType()
        switch (userType) {
        case UserType.HOMEOWNER_USER:
            if let hRecord = Globals.globObj.homeRecord {
                ParseHomeOwner.updateUserInsightsWithRegToken(hRec: hRecord, token: token)
            }
            break
        case UserType.FARMER_USER:
            // Farm User
            break
        case UserType.COURIER_USER:
            // Courier User
            break
        default:
            print("unsupported userType")
            break
        }
        
    }
    
